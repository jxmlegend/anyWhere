    MMP_ERR mmpstatus;

    MMPH_HIF_WaitSem(GRP_IDX_VID, 0);
    mmpstatus = MMPH_HIF_SendCmd(GRP_IDX_VID, HIF_VID_CMD_VIDPLAY_INFO | VIDPLAY_GET_PARAMETER | VIDPARAM_SUPPORTED_FORMAT);

