//------------------------------------------------------------------------------
//  Function    : MMPD_VIDPLAYER_CloseCodec
//  Description :
//------------------------------------------------------------------------------
/** @brief Playback control: Close

@pre The firmware is loaded and free to response
@return The status from the firmware
*/
MMP_ERR MMPD_VIDPLAYER_CloseCodec(void)
{
    MMP_ERR mmpstatus;

    MMPD_System_ResetHModule(MMPD_SYS_MDL_H264, MMP_TRUE);
            
    if(m_ubConfigMem == MMP_FALSE){
        MMPD_System_EnableClock(MMPD_SYS_CLK_H264, MMP_TRUE);
        MMPD_System_EnableClock(MMPD_SYS_CLK_JPG, MMP_TRUE);
    }
    
    MMPH_HIF_WaitSem(GRP_IDX_VID, 0);
    mmpstatus = MMPH_HIF_SendCmd(GRP_IDX_VID, HIF_VID_CMD_VIDEO_DECODER | CLOSE_CODEC);
    MMPH_HIF_ReleaseSem(GRP_IDX_VID);
    
    if(m_ubConfigMem == MMP_FALSE){
        MMPD_System_EnableClock(MMPD_SYS_CLK_H264, MMP_FALSE);
        MMPD_System_EnableClock(MMPD_SYS_CLK_JPG, MMP_FALSE);
    } 
    else {
        MMPD_System_EnableClock(MMPD_SYS_CLK_H264, MMP_FALSE);
        MMPD_System_EnableClock(MMPD_SYS_CLK_JPG, MMP_FALSE);
        if(m_ubVPPClkCtl & MMPD_3GPPLAY_VPP_DMAROTATE) {
            MMPD_System_EnableClock(MMPD_SYS_CLK_DMA, MMP_FALSE);
        }    
        if(m_ubVPPClkCtl & MMPD_3GPPLAY_VPP_GRAPHIC){
            MMPD_System_EnableClock(MMPD_SYS_CLK_GRA, MMP_FALSE);
            MMPD_System_EnableClock(MMPD_SYS_CLK_SCALE, MMP_FALSE);
            MMPD_System_EnableClock(MMPD_SYS_CLK_IBC, MMP_FALSE);
        }
    }
    
    m_ubConfigMem = MMP_FALSE;
    return mmpstatus;
}

